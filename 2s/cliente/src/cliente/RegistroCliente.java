    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String CUI = txtCUI.getText();
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        if (CantidadClientes > 5) {
            JOptionPane.showConfirmDialog(null, "Ya se han ingresado los 5 clientes");
            return;
        }
        for (int i = 0; i < clientes.length; i++) {
            if(clientes[i] != null) {
                if(clientes[i].getCui().equals(CUI)) {
                    JOptionPane.showConfirmDialog(null, "El cliente ya esta registrado");
                    return;
                }
            } else {
                Pattern pattern = Pattern.compile("[0-9]+");
                Matcher matcher = pattern.matcher(CUI);
                if(matcher.find()) {
                    clientes[i] = new ClienteClass(CUI,nombre,apellido);
                    CantidadClientes++;
                    txtCUI.setText("");
                    txtNombre.setText("");
                    txtApellido.setText("");
                    JOptionPane.showConfirmDialog(null, "Usuario registrado correctamente");
                    return;
                } else {
                    JOptionPane.showConfirmDialog(null, "El CUI deberÃ­a ser solo numerico");
                    return;
                }
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

