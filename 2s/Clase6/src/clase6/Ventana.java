    public void agregarNumero(String agregar) {
        if (reingresar) {
            pantalla.setText(agregar);
            reingresar = false;
        } else {
            String texto = pantalla.getText();
            texto += agregar;
            pantalla.setText(texto);
        }
    }

    public void agregarhistorial(int operacion, String resultado) {
        DefaultTableModel model = (DefaultTableModel) tableHistorial.getModel();
        String op = switch (operacion) {
            case 1 ->
                "Suma";
            case 2 ->
                "Resta";
            case 3 ->
                "Multiplicación";
            case 4 ->
                "División";
            default ->
                "Error";
        };
        String[] row = {op, resultado};
        model.addRow(row);      
    }

    public void Operar(int newop) {
        if ((reingresar && op != 0) || pantalla.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese un número");
            return;
        }
        String texto = pantalla.getText();
        try {
            double numero = Double.parseDouble(texto);
            if (numeroAIngresado && op > 0) {
                double res = switch (op) {
                    case 1 ->
                        numeroA + numero;
                    case 2 ->
                        numeroA - numero;
                    case 3 ->
                        numeroA * numero;
                    case 4 ->
                        numeroA / numero;
                    default ->
                        0;
                };
                pantalla.setText(String.valueOf(res));
                reingresar = true;
                numeroA = res;
                agregarhistorial(op, String.valueOf(res));
            } else if (!numeroAIngresado) {
                numeroA = numero;
                numeroAIngresado = true;
                pantalla.setText("");
            }
            op = newop;
        } catch (NumberFormatException e) {
            System.out.println("No ingreso correctamente el numero");
            JOptionPane.showMessageDialog(null, "No ingreso correctamente el número");
            pantalla.setText("");
        } catch (Exception e) {
            System.out.println("error inseperado: " + e.getMessage());
        }
    }

