    private void btnigualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnigualActionPerformed
        if (reingresar || pantalla.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese un número");
            return;
        }
        if (numeroAIngresado) {
            String texto = pantalla.getText();
            try {
                double numero = Double.parseDouble(texto);
                double res;
                res = switch (op) {
                    case 1 ->
                        numeroA + numero;
                    case 2 ->
                        numeroA - numero;
                    case 3 ->
                        numeroA * numero;
                    case 4 ->
                        numeroA / numero;
                    default ->
                        0;
                };
                pantalla.setText(String.valueOf(res));
                numeroA = res;
                reingresar = true;
                agregarhistorial(op, String.valueOf(res));
                op = 0;
            } catch (NumberFormatException e) {
                System.out.println("No ingreso correctamente el numero");
                JOptionPane.showMessageDialog(null, "No ingreso correctamente el número");
                pantalla.setText("");
            } catch (Exception e) {
                System.out.println("error inseperado: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se ingreso primer numero ni operación");
            pantalla.setText("");
        }
    }//GEN-LAST:event_btnigualActionPerformed

